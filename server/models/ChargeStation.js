const mongoose = require('mongoose');

const ChargeStationSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  location: {
    type: String,
    required: true,
  },
  lat: {
    type: Number,
    required: true,
  },
  lng: {
    type: Number,
    required: true,
  },
});

module.exports = mongoose.model('ChargeStation', ChargeStationSchema);
